//program that uses a switch statement to display the day of the week when given a number between 1 and 7.

let number = "1";

switch(number) {
    case '1' :
        console.log('its Monday');
        break;
    case '2' :
        console.log('its Tuesday');
        break;
    case '3' :
        console.log('its Wednesday');
        break;
    case '4' :
        console.log('its Thrusday');
        break;
    case '5' :
        console.log('its Friday');
        break;
    case '6' :
        console.log('its Saturday');
        break;
    case '7' :
        console.log('its Sunday');
        break;
    default:
        break;
}
